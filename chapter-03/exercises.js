function countChars(string, char) {
output = 0;
for(var i = 0; i < string.length; i++){
  if (string[i] === char) {
    output++;
  }
}
return output;
}
console.log(countChars('together', 't'));
console.log(countChars('beepbeepboop', 'e'));

